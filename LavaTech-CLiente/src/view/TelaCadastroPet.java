    private void jButtonCarregarImagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCarregarImagemActionPerformed
        FileNameExtensionFilter imageFilter = new FileNameExtensionFilter("Image files", ImageIO.getReaderFileSuffixes());
        jFileChooser.addChoosableFileFilter(imageFilter);
        if(jFileChooser.showOpenDialog(this) == jFileChooser.APPROVE_OPTION) {
            try {
                imagem = new Imagem(jFileChooser.getSelectedFile());
            } catch (FileNotFoundException ex) {
                Logger.getLogger(TelaCadastroPet.class.getName()).log(Level.SEVERE, null, ex);
            }
            labelImagem.setIcon(imagem.getImageIcon());
        }
    }//GEN-LAST:event_jButtonCarregarImagemActionPerformed

