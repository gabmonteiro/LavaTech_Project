    private void jBtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSalvarActionPerformed
        int codUsuario = ComboBoxUsuario.getSelectedIndex(jComboBox1);
        
        if(nomePet.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Peencha o nome");
            nomePet.requestFocus();
        }
        if(especie.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Peencha a especie");
            especie.requestFocus();
        }
        if(raca.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Peencha a raça");
            raca.requestFocus();
        }else {
            boolean result = false;
            if(pet != null) {
                Pet petEditar;
                if(imagem!= null) {
                    petEditar = new Pet(pet.getCodPet(), nomePet.getText(), especie.getText(), raca.getText(), new Usuario(codUsuario), imagem.getImagem());                                       
                } else {
                    petEditar = new Pet(pet.getCodPet(), nomePet.getText(), especie.getText(), raca.getText(), new Usuario(codUsuario), null);                                       
                }
                result = Principal.ccont.petEditar(petEditar);
            } else {
                Pet petInserir;
                if(imagem!= null) {
                    petInserir = new Pet(pet.getCodPet(), nomePet.getText(), especie.getText(), raca.getText(), new Usuario(codUsuario), imagem.getImagem());                                       
                } else {
                    petInserir = new Pet(pet.getCodPet(), nomePet.getText(), especie.getText(), raca.getText(), new Usuario(codUsuario), null);                                       
                }
                result = Principal.ccont.petInserir(petInserir);
            }
            
            if(result) {
                JOptionPane.showMessageDialog(this, "Pet adicionado/editado com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro adicionando/editando pet!");
            }
        }
        telaConsulta.atualizaTabela();
        dispose();
    }//GEN-LAST:event_jBtnSalvarActionPerformed

